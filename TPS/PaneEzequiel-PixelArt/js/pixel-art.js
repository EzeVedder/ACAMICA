function hex2rgb(c) {
  if (c[0] === '#') c = c.substr(1);
  var r = parseInt(c.slice(0,2), 16),
      g = parseInt(c.slice(2,4), 16),
      b = parseInt(c.slice(4,6), 16);
  return 'rgb('+ r +','+ g +','+ b +')';
}





